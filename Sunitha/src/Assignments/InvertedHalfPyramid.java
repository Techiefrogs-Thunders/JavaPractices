package Assignments;

public class InvertedHalfPyramid {
    public static void main(String[] args) {

      //Inverted Half pyramid with * and Num
         for(int a=6; a>=1; --a){
            for(int b=1; b<=a; ++b){
               System.out.print("*");
           System.out.print(b);
        }
       // System.out.println();
       //System.out.println("&");
       System.out.println();

    }
}
    
}
