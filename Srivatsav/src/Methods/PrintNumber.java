package Methods;

public class PrintNumber {

    void printn(int i,double j){
        System.out.println("Addition of int and double: "+ (i+j));
    }

    void printn(float i,double j){
        System.out.println("Addition of float and double: "+ (i+j));
    }
    
    void printn(double i,long j){
        System.out.println("Addition of double and long: "+ (i+j));
    }

    void printn(long i,int j){
        System.out.println("Addition of long and int: "+ (i+j));
    } 

    void printn(float i,long j){
        System.out.println("Addition of float and long: "+ (i+j));
    }

    void printn(int i,float j){
        System.out.println("Addition of int and float: "+ (i+j));
    }
    
}
