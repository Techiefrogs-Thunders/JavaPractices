public class Operators2 {
    public static void main(String[] args) {
        int a = 25;
        int b = -52;
        int c = 52;
        boolean h = false;
        boolean g = true;
        System.out.println(~a); // Negation(~) [-(n+1)]
        System.out.println(~b);
        System.out.println(~a + ~b);
        System.out.println(~b + ~c);
        System.out.println(~b - ~c);
        System.out.println(!h);
        System.out.println(!g);
    }
    
}
