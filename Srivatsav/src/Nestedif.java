public class Nestedif {
    public static void main(String[] args) {
        int x =10;
        int y =40;
        if(x==10){
            if(y!=40){
                System.out.println("Hello");
            }
            else{
                System.out.println("Byeee");
            }
            if (y==40) {
                System.out.println("Helloooo");
            }
        }
    }
    
}
